int	ft_atoi(const char *str)
{
	int	i;
	int	sign;
	int	digits;

	sign = 1;
	digits = 0;
	i = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == ' ')
		i++;
	if (str[i] == '+' || str[i] == '-')
	{
		if (str[i] == '-')
			sign = -1;
		i++;
	}
	while (str[i] == '0')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		digits = digits * 10 + (str[i] - 48);
		i++;
	}
	return (digits * sign);
}
